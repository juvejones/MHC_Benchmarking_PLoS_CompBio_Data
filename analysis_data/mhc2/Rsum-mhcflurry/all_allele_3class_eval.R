### ANALYSIS ONE  ###
###This segment only used for calculating on summary data of each method
###Generate one summary table and VUS plots for every method
 outputfile <- "summary2_3c_1000nM.txt"
 df <- data.frame(methods)
 vus_col <- list()
 vus_ci_col <- list()
 Se_col <- list()
 for (i in 1:length(methods)){
   method <- methods[i]
   print(paste(method, "------>"))
   file <- paste("../",method,"/Rdata/summary.txt", sep="")
   datafile <- read.table(file, col.names=c("peptide","binding_core","meas_nm","meas_bi",
                                            "meas_contin","predict","predict_rank"),sep="\t", 
                                            header= FALSE, comment.char="#")
   
   ###Set up the input data and classify into three classes
   data <- data.frame(datafile)
   headerline <- which (with(data,peptide=="peptide"))
   data <- data[-headerline,]
   Binding <- list()
   for (j in 1:length(data$meas_nm)){
     if (as.numeric(as.character(data$meas_nm[j]))<50){
       Binding[j]="SB"
     } 
     else if (as.numeric(as.character(data$meas_nm[j]))<1000){
       Binding[j]="WB"
     } 
     else {
       Binding[j]="NB"
     }
   }
   data$binding <- Binding
   group <- data$binding
   
   ## Three of the methods use RANK as affinity meas and should be taken care of
   if (method == "consensus" || method == "comblib" || method == "tepitope"){
     factor <- 1-as.numeric(as.character(data$predict_rank))
     x <- factor[group=="NB"]
     y <- factor[group=="WB"]
     z <- factor[group=="SB"]
     ## Use predicted rank as threshold
     Se_col[i] <- Sp.Sm.Se(x,y,z,0.98,0.995)[3]
   }
   else {
     factor <- as.numeric(as.character(data$predict))
     x <- factor[group=="NB"]
     y <- factor[group=="WB"]
     z <- factor[group=="SB"]
     ## Use predicted binding affinity as threshold
     Se_col[i] <- Sp.Sm.Se(x,y,z,0.362,0.638)[3]
   }
   
   ###Calculate VUS and VUS_CI
   #vus <- VUS(x,y,z,method="Normal",p=0,q=0,alpha=0.05)
   vus <- Normal.VUS(x,y,z,p=0,q=0,alpha=0.05)
   vus_col[i] <- vus$estimate
   vus_ci_col[i] <- (vus$estimate - vus$CI[1])
 
   ###Plot the data generated by VUS; only needed for summary data
   #pdf(paste(method,"_3c_eval.pdf",sep=""),width=8,height=4)
   #plot(vus)
   #dev.off()
 }
 
 ###Write the final table
 df$vus <- vus_col
 df$vusci <- vus_ci_col
 df$Se <- Se_col
 write.table(as.matrix(df), outputfile, 
             col.names=c("Method", "VUS", "CI_L", "Se"), 
             row.names=F, append=T, sep="\t")

### ANALYSIS TWO  ###
###This part do calculation on individual allele
###Do calculation for each method and generate respective allele summary
for (i in 1:length(methods)){
  method <- methods[i]
  print(paste(method, "------>"))
  outputfile <- paste(method, "_vus.txt", sep="")
  df <- data.frame(allele_list[[1]])
  vus_col <- list()
  vus_ci_col <- list()
  Se_col <- list()
  
  ###Inner loop on each allele; read allele data
  for (j in 1:length(allele_list[[1]])){
    allele <- allele_list[[1]][j]
    file <- paste("../",method,"/Rdata/",allele,".txt", sep="")
    if (file.exists(file)){
      datafile <- read.table(file, col.names=c("peptide","binding_core","meas_nm",
                                               "meas_bi","meas_contin","predict",
                                               "predict_rank"),
                             sep="\t", header= FALSE, comment.char="#",skip=1)
    }
    else{
      print(paste("file not exist for allele:", allele))
      vus_col[j] <- NA
      Se_col[j] <- NA
      next
    } 
    
    ###Set up the input data and classify into three classes
    data <- data.frame(datafile)
    #headerline <- which (with(data,peptide=="peptide"))
    #data <- data[-headerline,]
    Binding <- list()
    for (k in 1:length(data$meas_nm)){
      if (as.numeric(as.character(data$meas_nm[k]))<50){
        Binding[k]="SB"
      } 
      else if (as.numeric(as.character(data$meas_nm[k]))<1000){
        Binding[k]="WB"
      } 
      else {
        Binding[k]="NB"
      }
    }
    data$binding <- Binding
    group <- data$binding
    ## Three of the methods use RANK as affinity meas and should be taken care of
    if (method == "consensus" || method == "comblib" || method == "tepitope"){
      factor <- 1-as.numeric(as.character(data$predict_rank))
    }
    else {
      factor <- as.numeric(as.character(data$predict))
    }
    
    x <- factor[group=="NB"]
    y <- factor[group=="WB"]
    z <- factor[group=="SB"]
    
    ###Calculate VUS
    vus_col[j] <- tryCatch(
      {
        vus <- Normal.VUS(x,y,z,p=0,q=0,alpha=0.05)
        vus_col[j] <- vus$estimate
      },
      error=function(cond){
        message(paste(cond, "\n", sep=""))
        return(NA)
      }
    )
    
    ###Calculate specificity
    if (method == "consensus" || method == "comblib" || method == "tepitope"){
      Se_col[j] <- tryCatch(
        {
          Se_col[j] <- Sp.Sm.Se(x,y,z,0.98,0.995)[3]
        },
        error=function(cond){
          message(paste(cond, "\n", sep=""))
          return(NA)
        }
      )
    }
    else {
      Se_col[j] <- tryCatch(
        {
          Se_col[j] <- Sp.Sm.Se(x,y,z,0.362,0.638)[3]
        },
        error=function(cond){
          message(paste(cond, "\n", sep=""))
          return(NA)
        }
      )
    }
    vus_col[j] <- format(vus_col[j], digits=4)
    Se_col[j] <- format(Se_col[j], digits=4)
  }
  df$vus <- vus_col
  df$Se <- Se_col
  write.table(as.matrix(df), outputfile, col.names=c("Allele", "VUS", "Se"), 
              row.names=F, append=F, sep="\t")
}



